package com.movie.VO;

public class MTime {
	int movie;
	String time;
	
	
	public MTime() {
		super();
	}
	public MTime(int movie, String time) {
		super();
		this.movie = movie;
		this.time = time;
	}
	public int getMovie() {
		return movie;
	}
	public void setMovie(int movie) {
		this.movie = movie;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
	
}
